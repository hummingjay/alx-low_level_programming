#include <stdio.h>
#include "3-calc.h"

/**
 * op_add - returns sum
 * @a: first integer
 * @b: second integer
 * Return: 0
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns subtraction
 * @a: first integer
 * @b: second integer
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns multiplication
 * @a: first integer
 * @b: second integer
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns division
 * @a: first integer
 * @b: second integer
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns modulo
 * @a: first integer
 * @b: second integer
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
